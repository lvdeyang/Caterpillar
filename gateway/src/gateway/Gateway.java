	/**
		 * The doGet method of the servlet. <br>
		 *
		 * This method is called when a form has its tag value method equals to get.
		 * 
		 * @param request the request send by the client to the server
		 * @param response the response send by the server to the client
		 * @throws ServletException if an error occurred
		 * @throws IOException if an error occurred
		 */
	public void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        String stream = request.getParameter("stream");
		InputStream inStream = new FileInputStream(new File(this.getClass().getResource("/").getPath()+"config.properties"));
		Properties prop = new Properties();  
		prop.load(inStream);
		String[] ips=prop.get("iplist").toString().split(",");
		String[] rates=prop.get("rates").toString().split(",");
		double d = Math.random();
		for (int i=0;i<rates.length;i++) {
			
			if(d<Double.parseDouble(rates[i])){
				response.sendRedirect("http://"+ips[i]+":6690/"+stream);
				System.out.println("mr say:redirect to "+ips[i]);
				return;
			}
		}
		
	}

